// Crie um programa que compare dois números e
//  diga qual é o maior. 
// Saída esperada (para 20 e 35):
// 35 é maior que 20

public class Exercicio4 {
    public static void main(String[] args) {
        int x = 20;
        int y = 35;
        if (y > x){
            System.out.print(y+" eh maior que "+x);
        } else{
            System.out.print(x+" nao eh maior que "+y);
        }

    }  
}
