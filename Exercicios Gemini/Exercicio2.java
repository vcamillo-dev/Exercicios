// Escreva um código que verifique se um número
// é par ou ímpar usando uma estrutura
// condicional.
// Saída esperada (para o número 7):
// O número 7 é Ímpar
public class Exercicio2 {
    public static void main(String[] args) {
        int x = 7;
        if (x % 2 == 0) {
            System.out.println("O número " + x + " é Par");
        } else {
            System.out.println("O número " + x + " é Ímpar");
        }
    }
}
